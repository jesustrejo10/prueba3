    private void BTNLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNLogInActionPerformed

        try{
        String Usuario = TXTUsuario.getText();
        String Password = TXTPass.getText();
        boolean continuar = VentanaLogInControlador.LogIn(Usuario, Password);
            if (continuar){
                JOptionPane.showMessageDialog(rootPane,"Bienvenido "+Usuario);
                Logeado =Usuario;
                this.dispose();
                VentanaMenuPrincipal nueva = new VentanaMenuPrincipal();
                nueva.setVisible(true);
                
            }
            else
                JOptionPane.showMessageDialog(rootPane,"USUARIO NO ENCONTRADO");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane,"ERROR EN CONSULTA BD -> "+e.getMessage());
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_BTNLogInActionPerformed

